import { createRequire } from "node:module";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { getIconData } from "@iconify/utils";

const require = createRequire(import.meta.url);
const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const COLLECTION_PACKAGES = {
  "vscode-icons": "@iconify-json/vscode-icons",
  logos: "@iconify-json/logos",
  "flat-color-icons": "@iconify-json/flat-color-icons",
  catppuccin: "@iconify-json/catppuccin",
  openmoji: "@iconify-json/openmoji",
  "file-icons": "@iconify-json/file-icons",
  mdi: "@iconify-json/mdi",
  ph: "@iconify-json/ph",
  octicon: "@iconify-json/octicon"
};

const PREFIX_ALIASES = {
  "vvscode-icons": "vscode-icons"
};

const ICON_NAME_ALIASES = {
  "seti:spring": "mdi:sprout"
};

const offlineMappingPath = resolve(__dirname, "../src/offlineIconify.ts");
const vuepressRulesPath = resolve(__dirname, "../src/generated/vuepressFileIcons.ts");

function normalizeIconName(iconName) {
  const directAlias = ICON_NAME_ALIASES[iconName];
  if (directAlias) {
    return directAlias;
  }

  const separator = iconName.indexOf(":");
  if (separator === -1) {
    return iconName;
  }
  const prefix = iconName.slice(0, separator);
  const suffix = iconName.slice(separator + 1);
  const normalizedPrefix = PREFIX_ALIASES[prefix] ?? prefix;
  return `${normalizedPrefix}:${suffix}`;
}

function collectIconNamesFromFiles(filePaths) {
  const names = new Set();
  const pattern = /["']([a-z0-9-]+:[^"']+)["']/gi;

  for (const filePath of filePaths) {
    const text = readFileSync(filePath, "utf8");

    let match = pattern.exec(text);
    while (match) {
      names.add(normalizeIconName(match[1]));
      match = pattern.exec(text);
    }
    pattern.lastIndex = 0;
  }

  return [...names].sort();
}

function loadCollections(iconNames) {
  const prefixes = [...new Set(iconNames.map((name) => name.slice(0, name.indexOf(":"))))].sort();
  const collections = {};
  const unsupportedPrefixes = [];

  for (const prefix of prefixes) {
    const packageName = COLLECTION_PACKAGES[prefix];
    if (!packageName) {
      unsupportedPrefixes.push(prefix);
      continue;
    }

    try {
      collections[prefix] = require(`${packageName}/icons.json`);
    }
    catch (error) {
      throw new Error(`Failed to load icon collection for prefix '${prefix}' from '${packageName}': ${String(error)}`);
    }
  }

  if (unsupportedPrefixes.length > 0) {
    throw new Error(`Unsupported icon prefixes in rules: ${unsupportedPrefixes.join(", ")}`);
  }

  return collections;
}

const iconNames = collectIconNamesFromFiles([offlineMappingPath, vuepressRulesPath]);
const collections = loadCollections(iconNames);

function buildSvg(iconData, collection) {
  const left = Number.isFinite(iconData.left) ? iconData.left : 0;
  const top = Number.isFinite(iconData.top) ? iconData.top : 0;
  const width = Number.isFinite(iconData.width)
    ? iconData.width
    : Number.isFinite(collection.width)
      ? collection.width
      : 16;
  const height = Number.isFinite(iconData.height)
    ? iconData.height
    : Number.isFinite(collection.height)
      ? collection.height
      : 16;

  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${left} ${top} ${width} ${height}" aria-hidden="true" focusable="false">${iconData.body}</svg>`;
}

function resolveIconSvg(fullName) {
  const separator = fullName.indexOf(":");
  if (separator === -1) {
    return null;
  }

  const prefix = fullName.slice(0, separator);
  const name = fullName.slice(separator + 1);
  const collection = collections[prefix];
  if (!collection) {
    return null;
  }

  const iconData = getIconData(collection, name);
  if (!iconData) {
    return null;
  }

  return buildSvg(iconData, collection);
}

const iconMap = {};
const missing = [];

for (const iconName of iconNames) {
  const svg = resolveIconSvg(iconName);
  if (!svg) {
    missing.push(iconName);
    continue;
  }
  iconMap[iconName] = svg;
}

if (missing.length > 0) {
  throw new Error(`Missing offline icons: ${missing.join(", ")}`);
}

const sortedEntries = Object.entries(iconMap).sort((a, b) => a[0].localeCompare(b[0]));
const lines = [
  "/* AUTO-GENERATED FILE. DO NOT EDIT. */",
  "/* Generated by scripts/generate-offline-icons.mjs */",
  "",
  "export const OFFLINE_ICON_SVGS: Record<string, string> = {",
  ...sortedEntries.map(([name, svg]) => `  ${JSON.stringify(name)}: ${JSON.stringify(svg)},`),
  "};",
  ""
];

const outputPath = resolve(__dirname, "../src/generated/offlineIconData.ts");
writeFileSync(outputPath, `${lines.join("\n")}`, "utf8");
console.log(`Generated ${sortedEntries.length} offline icons -> ${outputPath}`);
