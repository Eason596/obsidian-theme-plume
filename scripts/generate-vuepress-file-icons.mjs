import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const sourcePath = resolve(__dirname, "../../vuepress-theme-plume/plugins/plugin-md-power/src/node/fileIcons/definitions.ts");
const outputPath = resolve(__dirname, "../src/generated/vuepressFileIcons.ts");

const SECTION_NAMES = ["named", "folders", "files", "extensions", "partials"];

const PREFIX_ALIASES = {
  "vvscode-icons": "vscode-icons"
};

const ICON_NAME_ALIASES = {
  "seti:spring": "mdi:sprout"
};

function normalizeIconName(iconName) {
  const directAlias = ICON_NAME_ALIASES[iconName];
  if (directAlias) {
    return directAlias;
  }

  const separator = iconName.indexOf(":");
  if (separator === -1) {
    return iconName;
  }
  const prefix = iconName.slice(0, separator);
  const suffix = iconName.slice(separator + 1);
  const normalizedPrefix = PREFIX_ALIASES[prefix] ?? prefix;
  return `${normalizedPrefix}:${suffix}`;
}

function extractSectionObject(text, sectionName) {
  const marker = `${sectionName}: {`;
  const markerIndex = text.indexOf(marker);
  if (markerIndex === -1) {
    return {};
  }

  const openBraceIndex = text.indexOf("{", markerIndex);
  if (openBraceIndex === -1) {
    return {};
  }

  let depth = 0;
  let closeBraceIndex = -1;
  for (let index = openBraceIndex; index < text.length; index += 1) {
    const currentChar = text[index];
    if (currentChar === "{") {
      depth += 1;
      continue;
    }
    if (currentChar === "}") {
      depth -= 1;
      if (depth === 0) {
        closeBraceIndex = index;
        break;
      }
    }
  }

  if (closeBraceIndex === -1) {
    return {};
  }

  const body = text.slice(openBraceIndex + 1, closeBraceIndex);
  const entryPattern = /'((?:\\'|[^'])*)'\s*:\s*'((?:\\'|[^'])*)'/g;
  const result = {};

  let match = entryPattern.exec(body);
  while (match) {
    const rawKey = match[1].replace(/\\'/g, "'");
    const rawValue = match[2].replace(/\\'/g, "'");
    const key = rawKey.toLowerCase();
    const value = normalizeIconName(rawValue);

    // Preserve the first definition to keep source priority order.
    if (result[key] === undefined) {
      result[key] = value;
    }

    match = entryPattern.exec(body);
  }

  return result;
}

function serializeRecord(record, indent = "    ") {
  const lines = [];
  for (const [key, value] of Object.entries(record)) {
    lines.push(`${indent}${JSON.stringify(key)}: ${JSON.stringify(value)},`);
  }
  return lines;
}

function buildEmptyRules() {
  return {
    named: {},
    folders: {},
    files: {},
    extensions: {},
    partials: {}
  };
}

function writeOutput(rules) {
  const lines = [
    "/* AUTO-GENERATED FILE. DO NOT EDIT. */",
    "/* Generated by scripts/generate-vuepress-file-icons.mjs */",
    "",
    "export interface VuepressFileIconRules {",
    "  named: Record<string, string>;",
    "  folders: Record<string, string>;",
    "  files: Record<string, string>;",
    "  extensions: Record<string, string>;",
    "  partials: Record<string, string>;",
    "}",
    "",
    "export const VUEPRESS_FILE_ICON_RULES: VuepressFileIconRules = {",
    "  named: {",
    ...serializeRecord(rules.named),
    "  },",
    "  folders: {",
    ...serializeRecord(rules.folders),
    "  },",
    "  files: {",
    ...serializeRecord(rules.files),
    "  },",
    "  extensions: {",
    ...serializeRecord(rules.extensions),
    "  },",
    "  partials: {",
    ...serializeRecord(rules.partials),
    "  },",
    "};",
    ""
  ];

  writeFileSync(outputPath, `${lines.join("\n")}`, "utf8");
}

if (!existsSync(sourcePath)) {
  if (existsSync(outputPath)) {
    console.warn(`VuePress definitions not found at ${sourcePath}. Keeping existing generated rules.`);
    process.exit(0);
  }
  writeOutput(buildEmptyRules());
  console.warn(`VuePress definitions not found at ${sourcePath}. Generated empty fallback rules because no existing output was found.`);
  process.exit(0);
}

const sourceText = readFileSync(sourcePath, "utf8");
const rules = buildEmptyRules();
for (const sectionName of SECTION_NAMES) {
  rules[sectionName] = extractSectionObject(sourceText, sectionName);
}

writeOutput(rules);
const totalEntries = SECTION_NAMES.reduce((total, sectionName) => total + Object.keys(rules[sectionName]).length, 0);
console.log(`Generated ${totalEntries} VuePress file-icon rules -> ${outputPath}`);
